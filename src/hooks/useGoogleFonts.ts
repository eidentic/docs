// Extract font family name from CSS font-family string
function extractFontFamily(fontString: string | null | undefined): string | null {
  if (!fontString) return null;
  
  // Get the first font from the comma-separated list
  const firstFont = fontString.split(',')[0].trim();
  
  // Remove quotes if present
  const cleanFont = firstFont.replace(/['"]/g, '');
  
  return cleanFont;
}

// Track loaded fonts globally to avoid duplicate loads
const loadedFonts = new Set<string>();
let allFontsLoaded = false;

// Load all available fonts (for font picker preview)
export function loadAllFonts() {
  if (typeof window === 'undefined' || allFontsLoaded) return;
  
  const fontsToLoad = Object.keys(GOOGLE_FONTS).filter(f => !loadedFonts.has(f));
  if (fontsToLoad.length === 0) {
    allFontsLoaded = true;
    return;
  }

  // Dynamically import WebFont only on client side
  import('webfontloader').then((WebFont) => {
    // Load fonts in batches to avoid overwhelming the browser
    const batchSize = 50;
    const batches: string[][] = [];
    for (let i = 0; i < fontsToLoad.length; i += batchSize) {
      batches.push(fontsToLoad.slice(i, i + batchSize));
    }

    batches.forEach((batch, index) => {
      setTimeout(() => {
        try {
          WebFont.load({
            google: {
              families: batch.map((f) => GOOGLE_FONTS[f]),
            },
            active: () => {
              batch.forEach((f) => loadedFonts.add(f));
              if (index === batches.length - 1) {
                allFontsLoaded = true;
              }
            },
          });
        } catch (error) {
          console.error('Failed to load Google Fonts batch:', error);
        }
      }, index * 100);
    });
  }).catch((error) => {
    console.error('Failed to load WebFont library:', error);
  });
}

export function useGoogleFonts(headingFont?: string | null, bodyFont?: string | null) {
  const loadedRef = useRef<Set<string>>(new Set());

  useEffect(() => {
    // Only run on client side
    if (typeof window === 'undefined') {
      return;
    }

    const fontsToLoad: string[] = [];
    const headingFamily = extractFontFamily(headingFont);
    const bodyFamily = extractFontFamily(bodyFont);



    [headingFamily, bodyFamily].forEach((family) => {
      if (family && GOOGLE_FONTS[family] && !loadedFonts.has(family) && !loadedRef.current.has(family)) {
        fontsToLoad.push(family);
        loadedRef.current.add(family);

      } else if (family && !GOOGLE_FONTS[family]) {
        console.warn('[Font Loader] Font not found in GOOGLE_FONTS map:', family);
      }
    });

    if (fontsToLoad.length === 0) {

      return;
    }



    // Dynamically import WebFont only on client side
    import('webfontloader').then((WebFont) => {
      try {
        WebFont.load({
          google: {
            families: fontsToLoad.map((f) => GOOGLE_FONTS[f]),
          },
          active: () => {
            fontsToLoad.forEach((f) => loadedFonts.add(f));
          },
          inactive: () => {
            fontsToLoad.forEach((f) => loadedRef.current.delete(f));
            console.warn('[Font Loader] Failed to load fonts:', fontsToLoad);
          },
        });
      } catch (error) {
        console.error('[Font Loader] Failed to load Google Fonts:', error);
      }
    }).catch((error) => {
      console.error('[Font Loader] Failed to load WebFont library:', error);
    });
  }, [headingFont, bodyFont]);
}


